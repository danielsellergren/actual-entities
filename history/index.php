    <h3>Actual Entities</h3>

    <h4>What is an Actual Entity?</h4>

    <p>According to Whitehead, the universe does not consist of substances, but rather an abundance of processes which consist of "moments" or "occasions". These "occasions" are the actual entities themselves. Along with the AEs, the universe also consists of two other types known as Eternal Objects (EOs) and a non-temporal being termed God.</p>

    <blockquote> An actual entity is a process of growth from phase to phase ending in a definite achievement. &mdash;Hooper</blockquote>

    <p>The growth from phase to phase is called "concrescence" with the final result being termed "concretion".</p>

    <img src="" />

    <p>Additionally, actual entities are not everyday objects. Neither the books on your shelf, nor the people in your life are individual actual entities. Instead, picture yourself and everyone else as great "societies" of actual entities. A multitude of actual entities working as atomic elements, but also part of a functional whole. A single actual entity is much closer to a quantum vibration than an organism.</p>

