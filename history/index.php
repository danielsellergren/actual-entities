    <h4>Positive versus Negative Prehensions</h4>

    <p>The prehensions discussed so far have all been positive prehensions. But there are also negative prehensions which mean that after the data has been objectified and interpreted, the subject entity deems that it is unimportant for development and rejects or ignores it. This decision may seem trivial, but not choosing to let something affect an entity is just as important as the decision to make changes.</p>

    <h4>Eternal Objects</h4>

    <p>The other objects in the universe other than actual entities are eternal objects, which do not prehend or have subjectivity, but can be prehended by each actual entity. The eternal object's purpose is to lend a definiteness to the actual entity, to provide it with a way of looking at the universe and interpreting what it sees. Essentially, the rules by which each actual entity makes the decision as to how an experience will affect them are determined by the definite eternal objects which were part of the cause of their initial novel synthesis.</p>

