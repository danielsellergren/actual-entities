    <h2>A Quick History of Actual Entities</h2>

    <p>Actual Entities are the fundamental elements of reality in the philosophy of English-American mathematician and logician, <a href="http://en.wikipedia.org/wiki/Alfred_North_Whitehead">Alfred North Whitehead</a>. His philosophical worldview is known as <a href="http://en.wikipedia.org/wiki/Process_philosophy">Process Philosophy</a> and is based on the ideas of change and development.</p>

    <img src="../img/whitehead.jpg" width="285" height="320" alt="Alfred North Whitehead" />

    <h3>Whitehead&rsquo;s Work</h3>

    <p>Whitehead is best known for his philosophical treatise <a href="http://en.wikipedia.org/wiki/Process_and_Reality">Process and Reality</a>, and for coauthoring with Bertrand Russell the magnificent <a href="http://en.wikipedia.org/wiki/Principia_Mathematica">Principia Mathematica</a> which is considered by many to be the seminal work of modern symbolic logic.</p>

    <p>In <em>Process and Reality</em>, Whitehead proposed that the universe is populated entirely by Actual Entities (AEs), Eternal Objects (EOs), and a principal object known as God. In this philosophical master work, all things are occasions of existence or "moments" and the process by which they change is the creative act of becoming.</p>

