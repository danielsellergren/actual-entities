    <h4>How is an Actual Entity created?</h4>

    <p>So how are Actual Entities created? Each Actual Entity is a creative act, that looks at every other object in the universe and then builds a new and unique reflection of the universe. This reflection of the immediate settled state of the universe is itself the Actual Entity.</p>

    <img src="" />

    <p>We can think about an Actual Entity in terms of its two sides: the subject and the object.</p>

    <p>The subject is the entity's immediate, private experience. When thinking about human beings this is generally thought of as consciousness, but it extends to a lesser extent in unconscious objects such as trees and rocks and then even further to the smallest societies of entities and their constituent occasions.</p>

    <p>The object is the sum of the experiences of the subject. It is the external part of the entity which has the potential for being experienced by other entities in the universe. When a subject looks at an object, that object is reflected in the creation of the process. The experience of any given subject is dependent on the experiences of the object it is looking at.</p>

