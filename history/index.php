    <h3>The Doctrine of Prehensions</h3>

    <h4>Causation</h4>

    <p>We should first note two distinct types of causation that occur in the process philosophy. The way we normally think of causality in the physical world is here termed "efficient causation" because it follows directly from past states and events. Additionally, there is "final causation" which is the causality stemming from the push toward the end goals of the process. Efficient causation is the potentiality of the other actual entities becoming part of the formation of a new entity. Final causation is the self-creative process within the subject actual entity that makes decisions towards its goals based on the data available.</p>

    <h4>Prehension</h4>

    <p>Prehension is the act of a subject actual entity experiencing the data and experiences of all the object actual entities (and eternal objects), being changed via efficient causation and also changing itself via final causation. In other words, one entity looks at all the others and makes a decision as to how to modify itself based on what it sees and what it wants to be.</p>

    <p>Think of a prehension as an operation with three steps:</p>

    <ol>
    	<li>Objectification of the data of the object entity.</li>
    	<li>Interpretation of the data by the subject entity.</li>
    	<li>Absorption of the data into the subject entity.</li>
    </ol>

